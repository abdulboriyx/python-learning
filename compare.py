x = int(input("What is x? "))
y = int(input("What is y? "))

# if
if x < y: 
      print("x is less than y")

if x > y: 
      print("x is greater than y")

if x == y: 
      print("x is equal to y")

# elif and else
if x < y: 
      print("x is less than y")
elif x > y: 
      print("x is greater than y")
else: 
      print("x is equal to y")

# or
if x < y or x > y:
      print("x is not equal to y")
else: 
      print("x is equal to y")

# !=
if x != y: 
      print("x is not equal to y")
else: 
      print("x is equal to y")